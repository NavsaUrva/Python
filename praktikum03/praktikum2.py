#program menghitung konversi suhu celcius ke fahrenheit ke reamur
#input
celcius = input("masukan suhu celcius = ")

#rumus
fahrenheit = 9/5 * int(celcius) + 32
reamur = 4/5 * int(celcius)

#output
print ("hasil konversi dari celcius ke fahrenheit =", fahrenheit)
print ("hasil konversi dari celcius ke reamur =", reamur)